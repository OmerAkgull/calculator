//arithmetic functions
function sum(a, b) {
  return a + b;
}
console.log(sum(1, 3));

function subtract(a, b) {
  return a - b;
}
console.log(subtract(5, 4));

function multiply(a, b) {
  return a * b;
}
console.log(multiply(5, 10));

function divide(a, b) {
  return a / b;
}
console.log(divide(60, 15));
